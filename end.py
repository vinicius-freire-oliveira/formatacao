# Imprime uma mensagem utilizando múltiplos argumentos, cada um representando uma palavra da frase.
# O parâmetro 'end' é utilizado para especificar o que será impresso no final da linha.
# No primeiro caso, 'end=' ' imprime um espaço em branco após a frase.
print ('Sport', 'Clube', 'do', 'Recife', 'é', 'campeão', 'estadual', '44', 'vezes', end=' ')

# Imprime uma mensagem similar à anterior, mas sem especificar o parâmetro 'end', portanto, a saída padrão é uma quebra de linha.
# O resultado é uma nova linha após a frase.
print ('Sport', 'Clube', 'do', 'Recife', 'é', 'campeão', 'estadual', '44', 'vezes', end='')

# Imprime uma mensagem similar às anteriores, mas com um valor específico para o parâmetro 'end' ('xyz').
# Isso significa que a saída será a frase seguida por 'xyz'.
print ('Sport', 'Clube', 'do', 'Recife', 'é', 'campeão', 'estadual', '44', 'vezes', end='xyz')

# Imprime uma mensagem similar às anteriores, mas utilizando '\n' como valor para o parâmetro 'end'.
# Isso significa que a saída será a frase seguida por uma nova linha.
print ('Sport', 'Clube', 'do', 'Recife', 'é', 'campeão', 'estadual', '44', 'vezes', end='\n')

# Imprime uma mensagem similar às anteriores, mas sem especificar o parâmetro 'end', resultando em uma quebra de linha padrão.
# O resultado é uma nova linha após a frase.
print ('Sport', 'Clube', 'do', 'Recife', 'é', 'campeão', 'estadual', '44', 'vezes')
